/**
메서드와 변수가 결합된 형태의 클래스
**/
public class UnitedData{
	public int x; //멤버 변수의 선언
	public int y; //멤버 변수의 선언
	
	public int plus(){ //멤버 메서드의 선언
		return x + y;
	}
	
	public int minus(){ //멤버 메서드의 선언
		return x - y;
	}

	public int divide(){ //멤버 메서드의 선언
		return x / y;
	}

	public int mul(){ //멤버 메서드의 선언
		return x * y;
	}
}


